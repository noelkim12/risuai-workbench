import fs from 'node:fs';
import path from 'node:path';

import {
  type LuaAstNode,
  type RisuLuaForbiddenDiagnostic,
  type RisuLuaForbiddenDiagnosticCode,
  type RisuLuaSourceLocation,
  analyzeRisuLuaForbiddenPatterns,
  getLocation,
  isIdentifierNamed,
  parseRisuLuaSource,
  walkLuaAst,
} from './risulua-forbidden-analyzer';
import { analyzeRisuLuaLocalBudget } from './risulua-local-budget-analyzer';
import type { RisuLuaBundledOutput } from './risulua-modular-bundler';
import {
  encodeRisuLuaRecoveryBlock,
  removeRisuLuaRecoveryBlock,
  type RisuLuaRecoveryManifest,
} from './risulua-recovery';
import type { RisuLuaBundleTarget } from './risulua-target';

/**
 * RisuLua 번들러가 생성한 dist 파일의 헤더 주석.
 * 이 파일은 자동으로 생성되므로 직접 수정하지 말고 lua/main.risulua를 다시 빌드할 것.
 */
export const RISULUA_DIST_GENERATED_HEADER = [
  '-- @generated by risu-workbench RisuLua modular bundler',
  '-- Do not edit directly; rebuild from lua/main.risulua.',
  '-- This dist is a flattened runtime artifact, not a source module-table input.',
  '-- To re-modularize, use lua/main.risulua or legacy/original.risulua instead.',
  '-- It may include embedded recovery metadata for packed archive restoration.',
  '',
].join('\n');

/**
 * RisuLua dist 파일에서 발생할 수 있는 오류 코드 목록.
 */
export type RisuLuaDistErrorCode =
  | 'forbidden_output'
  | 'local_budget'
  | 'missing_dist'
  | 'parse_error'
  | 'source_path_selected';

/**
 * RisuLua dist 파일 검증 결과의 진단 정보를 담는 인터페이스.
 */
export interface RisuLuaDistDiagnostic {
  /** 오류 코드. */
  code: RisuLuaDistErrorCode;
  /** 오류 메시지. */
  message: string;
  /** dist 파일의 절대 경로. */
  distPath: string;
  /** dist 파일의 상대 경로. */
  distRelativePath: string;
  /** 문제가 발생한 원본 파일 경로(선택적). */
  filePath?: string;
  /** 오류가 발생한 위치 정보(선택적). */
  location?: RisuLuaSourceLocation;
  /** local budget 진단이 측정된 scope 시작 위치(선택적). */
  scopeLocation?: RisuLuaSourceLocation;
  /** local budget 진단의 max active local 도달 위치(선택적). */
  peakLocation?: RisuLuaSourceLocation;
  /** 관련된 심볼 이름(선택적). */
  symbol?: string;
  /** 진단 심각도. 생략된 기존 진단은 validation에서 error처럼 처리됨. */
  severity?: 'warning' | 'error';
  /** local budget 진단의 local 개수. */
  localCount?: number;
  /** local budget 진단이 넘은 threshold. */
  threshold?: number;
  /** local budget hard limit. */
  limit?: number;
  /** local budget이 측정된 scope 종류. */
  scopeKind?: 'chunk' | 'function';
}

/**
 * RisuLua dist 파일을 작성할 때 필요한 옵션.
 */
export interface WriteRisuLuaDistOptions {
  /** 번들 대상 정보. */
  target: RisuLuaBundleTarget;
  /** 번들링된 출력물. 문자열 또는 번들 결과 객체를 받을 수 있음. */
  bundled: RisuLuaBundledOutput | string;
  /** dist 끝에 포함할 복원 메타데이터 manifest(선택적). */
  recoveryManifest?: RisuLuaRecoveryManifest;
}

/**
 * RisuLua dist 파일 작성 결과를 담는 인터페이스.
 */
export interface RisuLuaDistWriteResult {
  /** 작성된 dist 파일의 절대 경로. */
  distPath: string;
  /** 작성된 dist 파일의 상대 경로. */
  distRelativePath: string;
  /** 작성된 코드 내용. */
  code: string;
}

/**
 * RisuLua dist 파일을 검증할 때 필요한 옵션.
 */
export interface ValidateRisuLuaDistOptions {
  /** 번들 대상 정보. */
  target: RisuLuaBundleTarget;
  /** 선택된 소스 파일 경로 목록(선택적). */
  selectedPaths?: readonly string[];
}

/**
 * RisuLua dist 파일 검증 결과를 담는 인터페이스.
 */
export interface RisuLuaDistValidationResult {
  /** 검증된 dist 파일의 절대 경로. */
  distPath: string;
  /** 검증된 dist 파일의 상대 경로. */
  distRelativePath: string;
  /** 검증된 코드 내용. */
  code: string;
}

/**
 * RisuLua dist 파일 처리 중 발생한 오류를 나타내는 클래스.
 */
export class RisuLuaDistError extends Error {
  /** 상세 진단 정보. */
  readonly diagnostic: RisuLuaDistDiagnostic;

  constructor(diagnostic: RisuLuaDistDiagnostic) {
    super(diagnostic.message);
    this.name = 'RisuLuaDistError';
    this.diagnostic = diagnostic;
  }
}

/**
 * 번들링된 RisuLua 코드를 dist 파일로 작성함.
 * @param options - 작성 옵션.
 * @returns 작성 결과 정보.
 */
export function writeRisuLuaDist(options: WriteRisuLuaDistOptions): RisuLuaDistWriteResult {
  const { target } = options;
  const bundledCode = typeof options.bundled === 'string' ? options.bundled : options.bundled.code;
  const recoveryBlock = options.recoveryManifest
    ? `${bundledCode.endsWith('\n') ? '' : '\n'}${encodeRisuLuaRecoveryBlock(options.recoveryManifest)}`
    : '';
  const code = `${RISULUA_DIST_GENERATED_HEADER}${bundledCode}${recoveryBlock}`;

  // dist 파일이 들어갈 디렉토리 생성.
  fs.mkdirSync(path.dirname(target.distPath), { recursive: true });
  // 코드를 파일에 작성.
  fs.writeFileSync(target.distPath, code, 'utf-8');

  return {
    distPath: target.distPath,
    distRelativePath: target.distRelativePath,
    code,
  };
}

/**
 * RisuLua dist 파일이 올바르게 생성되었는지 검증함.
 * 소스 파일이 선택되었는지, 파일이 존재하는지, 금지된 패턴이 없는지 확인함.
 * @param options - 검증 옵션.
 * @returns 검증 결과 정보.
 * @throws {RisuLuaDistError} 검증에 실패하면 오류를 던짐.
 */
export function validateRisuLuaDist(
  options: ValidateRisuLuaDistOptions,
): RisuLuaDistValidationResult {
  const { target, selectedPaths = [] } = options;

  // 소스 파일이 선택되었는지 확인.
  const selectedSource = findSelectedLuaSourcePath(target, selectedPaths);
  if (selectedSource) {
    throw distError({
      code: 'source_path_selected',
      target,
      filePath: selectedSource,
      message: `RisuLua modular pack must select ${target.distRelativePath}, not source module ${toPosix(path.relative(target.rootDir, selectedSource))}`,
    });
  }

  // dist 파일이 존재하는지 확인.
  if (!fs.existsSync(target.distPath) || !fs.statSync(target.distPath).isFile()) {
    throw distError({
      code: 'missing_dist',
      target,
      filePath: target.distPath,
      message: `Missing RisuLua modular dist: expected ${target.distRelativePath} at ${target.distPath}`,
    });
  }

  // 파일 내용을 읽고 금지된 패턴을 분석.
  const code = fs.readFileSync(target.distPath, 'utf-8');
  const diagnostics = analyzeRisuLuaDistOutput({
    code,
    distPath: target.distPath,
    distRelativePath: target.distRelativePath,
  });
  const blockingDiagnostic = diagnostics.find((diagnostic) => diagnostic.severity !== 'warning');
  if (blockingDiagnostic) {
    throw new RisuLuaDistError(blockingDiagnostic);
  }

  return {
    distPath: target.distPath,
    distRelativePath: target.distRelativePath,
    code,
  };
}

/**
 * RisuLua dist 파일의 출력물을 분석하여 문제가 있는지 확인함.
 * 파싱 오류와 금지된 패턴(require 호출 등)을 검출함.
 * @param options - 분석 옵션.
 * @returns 발견된 진단 정보 배열.
 */
export function analyzeRisuLuaDistOutput(options: {
  code: string;
  distPath: string;
  distRelativePath: string;
}): RisuLuaDistDiagnostic[] {
  const { code, distPath, distRelativePath } = options;
  const analyzableCode = removeRisuLuaRecoveryBlock(code);
  const sourceDiagnostics = analyzeRisuLuaForbiddenPatterns({
    source: analyzableCode,
    filePath: distPath,
    moduleId: distRelativePath,
  });
  const parseDiagnostic = sourceDiagnostics.find((diagnostic) => diagnostic.code === 'parse_error');
  if (parseDiagnostic) {
    return [fromForbiddenDiagnostic(parseDiagnostic, distPath, distRelativePath, 'parse_error')];
  }

  let ast: LuaAstNode;
  try {
    ast = parseRisuLuaSource(analyzableCode);
  } catch (error) {
    const message = error instanceof Error ? error.message : String(error);
    return [
      {
        code: 'parse_error',
        distPath,
        distRelativePath,
        filePath: distPath,
        message: `Failed to parse RisuLua modular dist ${distRelativePath}: ${message}`,
      },
    ];
  }

  const diagnostics: RisuLuaDistDiagnostic[] = sourceDiagnostics
    .filter((diagnostic) => diagnostic.code !== 'parse_error')
    .map((diagnostic) =>
      fromForbiddenDiagnostic(diagnostic, distPath, distRelativePath, 'forbidden_output'),
    );

  // AST를 순회하며 require 호출을 검출.
  walkLuaAst(ast, (node) => {
    if (node.type === 'CallExpression' && isIdentifierNamed(node.base, 'require')) {
      diagnostics.push({
        code: 'forbidden_output',
        distPath,
        distRelativePath,
        filePath: distPath,
        location: getLocation(node) ?? undefined,
        symbol: 'require',
        message: `RisuLua modular dist must not contain executable require calls: ${distRelativePath}`,
      });
    }
  });

  const localBudgetCode = analyzableCode.startsWith(RISULUA_DIST_GENERATED_HEADER)
    ? analyzableCode.slice(RISULUA_DIST_GENERATED_HEADER.length)
    : analyzableCode;
  for (const diagnostic of analyzeRisuLuaLocalBudget({ code: localBudgetCode, filePath: distPath })) {
    diagnostics.push({
      code: 'local_budget',
      severity: diagnostic.severity,
      distPath,
      distRelativePath,
      filePath: distPath,
      location: diagnostic.location,
      scopeLocation: diagnostic.scopeLocation,
      peakLocation: diagnostic.peakLocation,
      symbol: 'local',
      localCount: diagnostic.localCount,
      threshold: diagnostic.threshold,
      limit: diagnostic.limit,
      scopeKind: diagnostic.scopeKind,
      message: diagnostic.message.replace(distPath, distRelativePath),
    });
  }

  return diagnostics.sort(compareDistDiagnostics);
}

/**
 * 선택된 경로들 중에서 소스 루트에 해당하는 .risulua 파일을 검색함.
 * @param target - 번들 대상 정보.
 * @param selectedPaths - 선택된 파일 경로 목록.
 * @returns 찾은 소스 파일 경로 또는 null.
 */
function findSelectedLuaSourcePath(
  target: RisuLuaBundleTarget,
  selectedPaths: readonly string[],
): string | null {
  const sourceRoot = path.resolve(target.sourceRoot);
  for (const selectedPath of selectedPaths) {
    const resolved = path.resolve(target.rootDir, selectedPath);
    const relative = path.relative(sourceRoot, resolved);
    if (
      relative &&
      !relative.startsWith('..') &&
      !path.isAbsolute(relative) &&
      resolved.endsWith('.risulua')
    ) {
      return resolved;
    }
  }
  return null;
}

/**
 * 금지된 패턴 진단 정보를 dist 진단 정보로 변환함.
 * @param diagnostic - 원본 금지 패턴 진단 정보.
 * @param distPath - dist 파일 경로.
 * @param distRelativePath - dist 파일 상대 경로.
 * @param code - 오류 코드.
 * @returns 변환된 dist 진단 정보.
 */
function fromForbiddenDiagnostic(
  diagnostic: RisuLuaForbiddenDiagnostic,
  distPath: string,
  distRelativePath: string,
  code: RisuLuaDistErrorCode,
): RisuLuaDistDiagnostic {
  const symbol = diagnostic.symbol ?? symbolForForbiddenCode(diagnostic.code);
  return {
    code,
    distPath,
    distRelativePath,
    filePath: distPath,
    location: diagnostic.location,
    symbol,
    message:
      code === 'parse_error'
        ? `Failed to parse RisuLua modular dist ${distRelativePath}: ${diagnostic.message}`
        : `RisuLua modular dist must not contain executable ${symbol ?? diagnostic.code}: ${distRelativePath}`,
  };
}

/**
 * 금지된 패턴 코드에 해당하는 심볼 이름을 반환함.
 * @param code - 금지된 패턴 코드.
 * @returns 해당하는 심볼 이름 또는 undefined.
 */
function symbolForForbiddenCode(code: RisuLuaForbiddenDiagnosticCode): string | undefined {
  if (
    code === 'dynamic_require' ||
    code === 'require_alias_or_wrapper' ||
    code === 'require_reassigned' ||
    code === 'require_shadowed'
  ) {
    return 'require';
  }
  return undefined;
}

/**
 * RisuLuaDistError를 생성함.
 * @param params - 오류 생성에 필요한 매개변수.
 * @returns 생성된 RisuLuaDistError 객체.
 */
function distError(params: {
  code: RisuLuaDistErrorCode;
  target: RisuLuaBundleTarget;
  filePath?: string;
  message: string;
}): RisuLuaDistError {
  return new RisuLuaDistError({
    code: params.code,
    distPath: params.target.distPath,
    distRelativePath: params.target.distRelativePath,
    filePath: params.filePath,
    message: params.message,
  });
}

/**
 * 두 dist 진단 정보를 위치와 코드 기준으로 비교함.
 * @param left - 첫 번째 진단 정보.
 * @param right - 두 번째 진단 정보.
 * @returns 비교 결과 숫자.
 */
function compareDistDiagnostics(left: RisuLuaDistDiagnostic, right: RisuLuaDistDiagnostic): number {
  return (
    (left.location?.line ?? 0) - (right.location?.line ?? 0) ||
    (left.location?.column ?? 0) - (right.location?.column ?? 0) ||
    left.code.localeCompare(right.code) ||
    (left.symbol ?? '').localeCompare(right.symbol ?? '')
  );
}

/**
 * 파일 경로를 POSIX 형식(슬래시 구분자)으로 변환함.
 * @param value - 변환할 파일 경로.
 * @returns POSIX 형식의 경로.
 */
function toPosix(value: string): string {
  return value.split(path.sep).join('/');
}
