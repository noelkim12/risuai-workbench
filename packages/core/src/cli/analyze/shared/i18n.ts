/** Analyzer report i18n — en/ko 2개 언어 지원 */

export type Locale = 'en' | 'ko';

/** argv에서 --locale 플래그를 파싱한다. 기본값 ko. */
export function detectLocale(argv: readonly string[]): Locale {
  const idx = argv.indexOf('--locale');
  if (idx >= 0 && argv[idx + 1] === 'en') return 'en';
  return 'ko';
}

/** placeholder 치환 — `{0}`, `{1}` 등을 순서대로 교체 */
export function t(locale: Locale, key: string, ...args: Array<string | number>): string {
  const dict = locale === 'ko' ? ko : en;
  let text = (dict as Record<string, string>)[key] ?? (en as Record<string, string>)[key] ?? key;
  for (let i = 0; i < args.length; i += 1) {
    text = text.replace(`{${i}}`, String(args[i]));
  }
  return text;
}

