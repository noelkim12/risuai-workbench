import { describe, expect, it } from 'vitest';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';

import {
  RISULUA_MODULE_TABLE_ASYNC_ACTIONS_PATH,
  RISULUA_MODULE_TABLE_COMMON_HELPERS_PATH,
  RISULUA_MODULE_TABLE_DOMAIN_CANDIDATES_PATH,
  RISULUA_MODULE_TABLE_GLOBAL_FUNCTIONS_PATH,
  RISULUA_MODULE_TABLE_REFACTOR_MAP_PATH,
  createEmptyRisuLuaModuleTableHostEffects,
  createRisuLuaModuleTableDomainCandidatesDocument,
  createRisuLuaModuleTableRefactorMapDocument,
  renderRisuLuaSplitReport,
  serializeRisuLuaModuleTableDomainCandidates,
  serializeRisuLuaModuleTableRefactorMap,
  writeRisuLuaModuleTableDomainCandidates,
  writeRisuLuaModuleTableRefactorMap,
  type RisuLuaModuleTableRefactorMapContract,
  type RisuLuaSplitPlan,
} from '../src/domain/risulua-split';
import type { LuaSourceRange } from '../src/domain/risulua-split';

describe('risulua-split module-table schema and report rendering', () => {
  it('renders module-table report sections without changing base report sections', () => {
    const refactorMap = createSyntheticRefactorMap();
    const report = renderRisuLuaSplitReport({
      plan: createSyntheticModuleTablePlan(),
      moduleTableRefactorMap: refactorMap,
      profileMap: ['Module-table synthetic fixture records report-only schema contracts.'],
      pureCandidates: ['trim -> lua/common/local_helpers.risulua'],
      runtimeCoupledHelpers: ['normalizeOutput -> lua/handler_helpers/output_helpers.risulua'],
      riskyBlocks: ['rerollChoices stays in main due to preserve:host-visible-global-unsafe-bridge'],
      dynamicPatterns: ['_G[dynamicName] preserved as preserve:dynamic-global-reference-risk'],
      refactorTasks: ['Review deck candidate before creating a domain module.'],
      verificationSuggestions: ['Validate docs/refactor-map.json and docs/domain-candidates.json.'],
    });

    expect(report).toContain('## Summary');
    expect(report).toContain('## Module-table extraction contract');
    expect(report).toContain(`- Refactor map: \`${RISULUA_MODULE_TABLE_REFACTOR_MAP_PATH}\``);
    expect(report).toContain(`- Domain candidates: \`${RISULUA_MODULE_TABLE_DOMAIN_CANDIDATES_PATH}\``);
    expect(report).toContain('### Runtime roots and modules');
    expect(report).toContain('`onOutput` (top-level-global-function, bridge:host-visible-global, lines 20-28)');
    expect(report).toContain('`lua/common/local_helpers.risulua` (common-helper, alias `__local_helpers`, exports `trim`)');
    expect(report).toContain('`lua/handler_helpers/output_helpers.risulua` (handler-helper, alias `__output_helpers`, exports `normalizeOutput`)');
    expect(report).toContain('`lua/host_globals/global_functions.risulua` (host-global, alias `__host_globals`, exports `onOutput`)');
    expect(report).toContain('`lua/host_globals/async_actions.risulua` (host-global, alias `__async_actions`, exports `runModel`)');
    expect(report).toContain('`onOutput` stays host-visible through direct assignment `onOutput = __host_globals.onOutput`');
    expect(report).toContain('`rerollChoices` preserved because `preserve:host-visible-global-unsafe-bridge`');
    expect(report).toContain('`proceduralInit` remains procedural report-only code because `preserve:top-level-side-effect`');
    expect(report).toContain('- reads: `getChatVar`, `getState`');
    expect(report).toContain('- writes: `setState`');
    expect(report).toContain('- uiInteraction: `alertNormal`, `reloadDisplay`');
    expect(report).toContain('- asyncModelNetwork: `LLM`, `request`');
    expect(report).toContain('- dynamicEnvironment: `_G`, `load`');
    expect(report).toContain('`deck` confidence 0.82; proposed target `lua/domain/deck.risulua`; generationStatus=report-only; autoGenerated=false');
    expect(report).toContain('`extract:pure-helper`');
    expect(report).toContain('`preserve:async-boundary-risk`');
    expect(report).toContain('## Validator results');
  });

  it('serializes first-class refactor-map and domain-candidates JSON documents', () => {
    const refactorMap = createSyntheticRefactorMap();
    const refactorJson = serializeRisuLuaModuleTableRefactorMap(refactorMap, { cwd: process.cwd() });
    const domainJson = serializeRisuLuaModuleTableDomainCandidates(refactorMap.domainCandidates, { cwd: process.cwd() });
    const refactorDocument = JSON.parse(refactorJson) as ReturnType<typeof createRisuLuaModuleTableRefactorMapDocument>;
    const domainDocument = JSON.parse(domainJson) as ReturnType<typeof createRisuLuaModuleTableDomainCandidatesDocument>;

    expect(refactorDocument).toEqual(expect.objectContaining({
      version: 1,
      mode: 'module-table',
      sourceFile: '<repo-root>/fixtures/source.risulua',
      source: { file: '<repo-root>/fixtures/source.risulua' },
    }));
    expect(refactorDocument.modules.map((moduleContract) => moduleContract.path)).toEqual([
      RISULUA_MODULE_TABLE_COMMON_HELPERS_PATH,
      'lua/handler_helpers/output_helpers.risulua',
      RISULUA_MODULE_TABLE_GLOBAL_FUNCTIONS_PATH,
      RISULUA_MODULE_TABLE_ASYNC_ACTIONS_PATH,
    ]);
    expect(refactorDocument.artifacts).toEqual(expect.arrayContaining([
      expect.objectContaining({ path: RISULUA_MODULE_TABLE_COMMON_HELPERS_PATH, exports: ['trim'] }),
      expect.objectContaining({ path: RISULUA_MODULE_TABLE_ASYNC_ACTIONS_PATH, exports: ['runModel'] }),
    ]));
    expect(refactorDocument.bridges).toEqual([expect.objectContaining({
      originalPublicName: 'onOutput',
      assignment: 'onOutput = __host_globals.onOutput',
    })]);
    expect(refactorDocument.hostEffects).toEqual({
      reads: ['getChatVar', 'getState'],
      writes: ['setState'],
      uiInteraction: ['alertNormal', 'reloadDisplay'],
      asyncModelNetwork: ['LLM', 'request'],
      dynamicEnvironment: ['_G', 'load'],
    });
    expect(refactorDocument.warnings).toEqual([]);
    expect(refactorDocument.domainCandidates.every((candidate) => candidate.autoGenerated === false)).toBe(true);
    expect(domainDocument).toEqual({
      version: 1,
      mode: 'module-table',
      candidates: refactorDocument.domainCandidates,
    });
    expect(domainDocument.candidates[0]).toEqual(expect.objectContaining({
      name: 'deck',
      confidence: 0.82,
      recommendedPath: 'lua/domain/deck.risulua',
      autoGenerated: false,
    }));
  });

  it('writes only docs JSON artifacts for module-table schema rendering', () => {
    withTempDir((outputRoot) => {
      const refactorMap = createSyntheticRefactorMap();
      const refactorWrite = writeRisuLuaModuleTableRefactorMap(refactorMap, { outputRoot, cwd: process.cwd() });
      const domainWrite = writeRisuLuaModuleTableDomainCandidates(refactorMap.domainCandidates, { outputRoot, cwd: process.cwd() });

      expect(refactorWrite.path).toBe(path.join(outputRoot, ...RISULUA_MODULE_TABLE_REFACTOR_MAP_PATH.split('/')));
      expect(domainWrite.path).toBe(path.join(outputRoot, ...RISULUA_MODULE_TABLE_DOMAIN_CANDIDATES_PATH.split('/')));
      expect(fs.existsSync(refactorWrite.path)).toBe(true);
      expect(fs.existsSync(domainWrite.path)).toBe(true);
      expect(fs.existsSync(path.join(outputRoot, 'lua'))).toBe(false);
      expect(fs.existsSync(path.join(outputRoot, 'legacy'))).toBe(false);
      expect(fs.existsSync(path.join(outputRoot, 'dist'))).toBe(false);
    });
  });
});

