function sourceRange(startLine: number, endLine: number, startOffset: number, endOffset: number): LuaSourceRange {
  return { startLine, endLine, startOffset, endOffset };
}

function withTempDir(run: (outputRoot: string) => void): void {
  const outputRoot = fs.mkdtempSync(path.join(os.tmpdir(), 'risulua-module-table-rendering-'));
  try {
    run(outputRoot);
  } finally {
    fs.rmSync(outputRoot, { recursive: true, force: true });
  }
}
